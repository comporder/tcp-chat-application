    private void logInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInActionPerformed

        if (nameTxt.getText().equals(null) || nameTxt.getText().equals("")) {
            System.out.println("FAIL!");
        } else {
            // Client servera baglanÄ±yor
            Client.startConnection("localhost", 5000);
            if (Client.socket == null) {
                JOptionPane.showMessageDialog(this, "Connection Failed !!");
                System.exit(0);
            }
            Message newMsg = new Message(Message.messageType.NAME);
            newMsg.content = nameTxt.getText();
            Client.sendRequest(newMsg);
            menuFrame.jLabel1.setText("Hi "+nameTxt.getText()+"!");
            menuFrame.setVisible(true);
            
        }


    }//GEN-LAST:event_logInActionPerformed

